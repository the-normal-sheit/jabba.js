function Coherence(text, corpus) {
  let score = 0;
  let weights = 0;
  
  let words = text.toLowerCase().match(/\b[\w']+\b/g) || [];
  let sentences = text.match(/[^.!?]+[.!?]+/g) || [text];
  
  if (words.length === 0) return 0;
  
  let corpusWords = new Set();
  let corpusBigrams = new Set();
  let corpusTrigrams = new Set();
  corpus.forEach(doc => {
    let cWords = doc.toLowerCase().match(/\b[\w']+\b/g) || [];
    cWords.forEach(w => corpusWords.add(w));
    for (let i = 0; i < cWords.length - 1; i++) {
      corpusBigrams.add(cWords[i] + " " + cWords[i + 1]);
    }
    for (let i = 0; i < cWords.length - 2; i++) {
      corpusTrigrams.add(cWords[i] + " " + cWords[i + 1] + " " + cWords[i + 2]);
    }
  });
  
  let vocabMatch = words.filter(w => corpusWords.has(w)).length / words.length;
  score += vocabMatch * 25;
  weights += 25;
  
  let bigramMatches = 0;
  for (let i = 0; i < words.length - 1; i++) {
    if (corpusBigrams.has(words[i] + " " + words[i + 1])) bigramMatches++;
  }
  if (words.length > 1) {
    score += (bigramMatches / (words.length - 1)) * 30;
    weights += 30;
  }
  
  let trigramMatches = 0;
  for (let i = 0; i < words.length - 2; i++) {
    if (corpusTrigrams.has(words[i] + " " + words[i + 1] + " " + words[i + 2])) trigramMatches++;
  }
  if (words.length > 2) {
    score += (trigramMatches / (words.length - 2)) * 20;
    weights += 20;
  }
  
  let avgSentenceLength = words.length / sentences.length;
  let sentenceLengthScore = Math.max(0, 100 - Math.abs(avgSentenceLength - 15) * 4);
  score += (sentenceLengthScore / 100) * 10;
  weights += 10;
  
  let uniqueWords = new Set(words);
  let lexicalDiversity = uniqueWords.size / words.length;
  let diversityScore = lexicalDiversity > 0.3 && lexicalDiversity < 0.9 ? 100 : 
                       Math.max(0, 100 - Math.abs(lexicalDiversity - 0.6) * 150);
  score += (diversityScore / 100) * 15;
  weights += 15;
  
  return Math.round((score / weights) * 100)/100;
}
