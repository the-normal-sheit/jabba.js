function getWordType(word) {
	word = word.toLowerCase();
	for(let type in posPatterns) {
		if(posPatterns[type].test(word)) return type;
	}
	return "unknown";
}

function grammarScore(word, prevWord, prevPrevWord) {
	let score = 1.0;
	let type = getWordType(word);
	let prevType = prevWord ? getWordType(prevWord) : null;
	let prevPrevType = prevPrevWord ? getWordType(prevPrevWord) : null;
	
	let rules = {
		"det-noun": 2.5,
		"adj-noun": 1.5,
		"noun-verb": 1.5,
		"adv-verb": 1.3,
		"verb-adv": 1.3,
		"prep-det": 1.5,
		"prep-noun": 2.5,
		"det-adj-noun": 2.5,
		"verb-prep": 1.3,
		"conj-noun": 1.2,
		"conj-verb": 1.2,
		"verb-noun":1.5,
		"det-verb": 0.3,
		"verb-verb": 0.5,
		"prep-prep": 0.1,
		"noun-det": 0.7
	};
	
	if(prevPrevType && prevType && type) {
		let trigramKey = `${prevPrevType}-${prevType}-${type}`;
		if(rules[trigramKey]) {
			score *= rules[trigramKey];
		}
	}
	
	if(prevType && type) {
		let bigramKey = `${prevType}-${type}`;
		if(rules[bigramKey]) {
			score *= rules[bigramKey];
		}
	}
	
	return score;
}

function Similarity(a, b) {
  if (a === b) return 1;
  if (a.length === 0 || b.length === 0) return 0;
  
  let matrix = [];
  
  for (let i = 0; i <= b.length; i++) {
    matrix[i] = [i];
  }
  
  for (let j = 0; j <= a.length; j++) {
    matrix[0][j] = j;
  }
  
  for (let i = 1; i <= b.length; i++) {
    for (let j = 1; j <= a.length; j++) {
      if (b.charAt(i - 1) === a.charAt(j - 1)) {
        matrix[i][j] = matrix[i - 1][j - 1];
      } else {
        matrix[i][j] = Math.min(
          matrix[i - 1][j - 1] + 1,
          matrix[i][j - 1] + 1,
          matrix[i - 1][j] + 1
        );
      }
    }
  }
  
  let distance = matrix[b.length][a.length];
  let maxLength = Math.max(a.length, b.length);
  
  return 1 - distance / maxLength;
}
